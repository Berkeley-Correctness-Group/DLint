/**
 * @dlintShort{Find code that refers to 'this' even though 'this' is the global object.}
 * @dlintLong{When 'this' is the global object, there is not need to use 'this' (and it maybe unintended).}
 * @dlintPattern{varRead(this,global)}
 * @dlintRule{Avoid referring to \code{this} when it equals to \code{global}.}
 * @dlintShortName{GlobalThis}
 * @dlintGroup{LanguageMisuse}
 * @dlintNeedDynamic
 * @dlintSingleEventPattern
 */

(function(sandbox) {
    function MyAnalysis() {
        var iidToLocation = sandbox.iidToLocation;
        var DLintWarning = sandbox.DLint.DLintWarning;

        var iidToCount = {};  // iid: number --> count: number

        var globalObject = (typeof window === "undefined" || sandbox.isPseudoDOM) ? global : window;

        this.read = function(iid, name, val, isGlobal, isPseudoGlobal) {
            if (name === "this" && val === globalObject) {
                iidToCount[iid] = (iidToCount[iid] | 0) + 1;
            }
        };

        this.endExecution = function() {
            var warnings = Object.keys(iidToCount).map(function(iid) {
                var location = iidToLocation(iid);
                return new DLintWarning("GlobalThis", iid, location, "'this' resolves to the global object at " + location + " " + iidToCount[iid] + " time(s).", iidToCount[iid]);
            });
            sandbox.DLint.addWarnings(warnings);
        };
    }
    sandbox.analysis = new MyAnalysis();
})(J$);
